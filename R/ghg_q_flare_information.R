    Y2_VOL_FLARE_GAS_METHOD = NULL, Y2_HHV = NULL, Y2ANNHHVALUOM = NULL, Y2_HHV_METHOD = NULL, 
    Y2_HHV_METHOD_OTHER = NULL, Y2_VOL_FLARE_GAS_CONDITIONS = NULL, Y2_HHV_CONDITIONS = NULL, 
