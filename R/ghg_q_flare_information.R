        CH4EMMVALUOM = CH4EMMVALUOM, FLARE_N2O_EMISSIONS = FLARE_N2O_EMISSIONS, N2OEMMVALUOM = N2OEMMVALUOM, 
        Y1A_MEASUREMENT_PERIOD = Y1A_MEASUREMENT_PERIOD, Y1A_VOL_FLARE_GAS = Y1A_VOL_FLARE_GAS, 
        Y1AEQVOLOFFLAREGASUOM = Y1AEQVOLOFFLAREGASUOM, Y1A_VOL_FLARE_GAS_METHOD = Y1A_VOL_FLARE_GAS_METHOD, 
        Y1A_MOL_WT = Y1A_MOL_WT, Y1AEQANNAVGMOLWTUOM = Y1AEQANNAVGMOLWTUOM, Y1A_MOL_WT_METHOD_OTHER = Y1A_MOL_WT_METHOD_OTHER, 
        Y1A_C_CONTENT = Y1A_C_CONTENT, Y1AEQANNAVGCCONTENTUOM = Y1AEQANNAVGCCONTENTUOM, 
        Y1A_C_CONTENT_METHOD_OTHER = Y1A_C_CONTENT_METHOD_OTHER, Y1B_MEASUREMENT_PERIOD = Y1B_MEASUREMENT_PERIOD, 
        Y1B_VOL_FLARE_GAS = Y1B_VOL_FLARE_GAS, Y1BANNVOLFLAREGASUOM = Y1BANNVOLFLAREGASUOM, 
        Y1B_VOL_FLARE_GAS_METHOD = Y1B_VOL_FLARE_GAS_METHOD, Y1B_CO2_CONCENTRATION = Y1B_CO2_CONCENTRATION, 
        Y1BANNAVGCO2CONCUOM = Y1BANNAVGCO2CONCUOM, Y1B_CO2_CONCENTRATION_METHOD = Y1B_CO2_CONCENTRATION_METHOD, 
        Y1B_CO2_CONCENTRA_METHOD_OTHER = Y1B_CO2_CONCENTRA_METHOD_OTHER, Y1B_NUMBER_NONCO2_C_COMPOUNDS = Y1B_NUMBER_NONCO2_C_COMPOUNDS, 
