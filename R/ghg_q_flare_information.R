    Y1B_CONC_NON_CO2_C_COMP_METHD = NULL, Y1BANNAVGCONCMTHD = NULL) {
    
    args <- list(FACILITY_ID = FACILITY_ID, REPORTING_YEAR = REPORTING_YEAR, C_FROM_CH4_FRACTION_BASIS = C_FROM_CH4_FRACTION_BASIS, 
        FLARE_TYPE = FLARE_TYPE, FLARE_TYPE_OTHER = FLARE_TYPE_OTHER, FLARE_SERVICE = FLARE_SERVICE, 
