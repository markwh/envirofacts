afs_effrs_afsplt1 <- function(EPA_PLANT_ID = NULL, PLANT_ID = NULL, REGION_CODE = NULL, 
    STATE_ABBREV = NULL, STATE_NAME = NULL, COUNTY_NAME = NULL, DUN_BRADSTREET_NUMBER = NULL, 
    STATE_COUNTY_COMPLIANCE_SOURCE = NULL, STATE_CODE = NULL, COUNTY_CODE = NULL, 
    CDS_PLANT_ID = NULL, PLANT_NAME = NULL, PLANT_STREET_ADDRESS = NULL, PLANT_CITY = NULL, 
    ZIP_CODE = NULL, PRIMARY_SIC_CODE = NULL, PRIMARY_SIC_DESCRIPTION = NULL, SECONDARY_SIC_CODE = NULL, 
    TERTIARY_SIC_CODE = NULL, SIC_EQUIVALENT_NAICS = NULL, NAICS_EQUIVALENT_SIC = NULL, 
    GOVT_FACILITY_CODE_DESC = NULL, NAICS_CODE = NULL, NAICS_CODE_DESCRIPTION = NULL, 
    STATE_REGISTRATION_NUMBER = NULL, LOCAL_CONTROL_REGION = NULL, LOCAL_CONTROL_REGION_DESC = NULL, 
    OPERATING_STATUS = NULL, OPERATING_STATUS_DESCRIPTION = NULL, HPV_FLAG = NULL, 
    DATE_PLANT_INFO_LAST_UPDATED = NULL, NAT_INIT_AIR_TOXICS = NULL, NAT_INIT_AIR_TOXICS_DESC = NULL, 
    AFS_PLANT_ID = NULL) {
    
    args <- list(EPA_PLANT_ID = EPA_PLANT_ID, PLANT_ID = PLANT_ID, REGION_CODE = REGION_CODE, 
        STATE_ABBREV = STATE_ABBREV, STATE_NAME = STATE_NAME, COUNTY_NAME = COUNTY_NAME, 
        DUN_BRADSTREET_NUMBER = DUN_BRADSTREET_NUMBER, STATE_COUNTY_COMPLIANCE_SOURCE = STATE_COUNTY_COMPLIANCE_SOURCE, 
