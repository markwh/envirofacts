        PLANT_NAME = PLANT_NAME, PLANT_STREET_ADDRESS = PLANT_STREET_ADDRESS, PLANT_CITY = PLANT_CITY, 
        ZIP_CODE = ZIP_CODE, PRIMARY_SIC_CODE = PRIMARY_SIC_CODE, PRIMARY_SIC_DESCRIPTION = PRIMARY_SIC_DESCRIPTION, 
        SECONDARY_SIC_CODE = SECONDARY_SIC_CODE, TERTIARY_SIC_CODE = TERTIARY_SIC_CODE, 
        SIC_EQUIVALENT_NAICS = SIC_EQUIVALENT_NAICS, NAICS_EQUIVALENT_SIC = NAICS_EQUIVALENT_SIC, 
        GOVT_FACILITY_CODE_DESC = GOVT_FACILITY_CODE_DESC, NAICS_CODE = NAICS_CODE, 
        NAICS_CODE_DESCRIPTION = NAICS_CODE_DESCRIPTION, STATE_REGISTRATION_NUMBER = STATE_REGISTRATION_NUMBER, 
        LOCAL_CONTROL_REGION = LOCAL_CONTROL_REGION, LOCAL_CONTROL_REGION_DESC = LOCAL_CONTROL_REGION_DESC, 
        OPERATING_STATUS = OPERATING_STATUS, OPERATING_STATUS_DESCRIPTION = OPERATING_STATUS_DESCRIPTION, 
        HPV_FLAG = HPV_FLAG, DATE_PLANT_INFO_LAST_UPDATED = DATE_PLANT_INFO_LAST_UPDATED, 
        NAT_INIT_AIR_TOXICS = NAT_INIT_AIR_TOXICS, NAT_INIT_AIR_TOXICS_DESC = NAT_INIT_AIR_TOXICS_DESC, 
        AFS_PLANT_ID = AFS_PLANT_ID)
