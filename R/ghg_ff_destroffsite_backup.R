        INDV_WEL_SHFT_DSTR_OFFST_TRNS3 = INDV_WEL_SHFT_DSTR_OFFST_TRNS3, INDV_WEL_SHFT_DSTR_OFFST_TRNS4 = INDV_WEL_SHFT_DSTR_OFFST_TRNS4, 
        INDV_WEL_SHFT_DSTR_OFFST_TRNS5 = INDV_WEL_SHFT_DSTR_OFFST_TRNS5, INDV_WEL_SHFT_DSTR_OFFST_TRNS6 = INDV_WEL_SHFT_DSTR_OFFST_TRNS6, 
        INDV_WEL_SHFT_DSTR_OFFST_TRNS7 = INDV_WEL_SHFT_DSTR_OFFST_TRNS7, INDV_WEL_SHFT_DSTR_OFFST_TRNS8 = INDV_WEL_SHFT_DSTR_OFFST_TRNS8)
