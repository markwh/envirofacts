        DESTR_EFFICIEN_BACKUP_2_DEVICE = DESTR_EFFICIEN_BACKUP_2_DEVICE, DESTR_EFFICIEN_PRIMARY_DEVICE = DESTR_EFFICIEN_PRIMARY_DEVICE, 
